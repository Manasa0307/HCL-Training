package com.src;

public class Delivery1 {
	int o,b,r;
	String ba,bo,no;
	public Delivery1() {
		super();
	}
	public Delivery1(int o, int b, int r, String ba, String bo, String no) {
		super();
		this.o = o;
		this.b = b;
		this.r = r;
		this.ba = ba;
		this.bo = bo;
		this.no = no;
	}
	public int getO() {
		return o;
	}
	public void setO(int o) {
		this.o = o;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public int getR() {
		return r;
	}
	public void setR(int r) {
		this.r = r;
	}
	public String getBa() {
		return ba;
	}
	public void setBa(String ba) {
		this.ba = ba;
	}
	public String getBo() {
		return bo;
	}
	public void setBo(String bo) {
		this.bo = bo;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	
}
	    